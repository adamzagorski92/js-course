console.log("Comparision Operators");

let var1 = 109;
let var2= "108";

/* Operator równości wartości */
if(var1==var2) {
    console.log("warunek " + var1 + "==" + var2 +" został spełniony! ")
}else {
    console.log("Warunek " + var1 + "==" + var2 +" nie został spełniony ")
}

/* Operator nierówności wartości */
if(var1!=var2) {
    console.log("warunek " + var1 + "!=" + var2 +" został spełniony!")
} else {
    console.log("Warunek " + var1 + "!=" + var2 +" nie został spełniony ")
}

/* Operator równości wartości i typów danych */
if(var1===var2) {
    console.log("warunek " + var1 + "===" + var2 +" został spełniony!")
} else {
    console.log("Warunek " + var1 + "===" + var2 +" nie został spełniony ")
}

/* Operator nierówności wartości i typów danych */
if(var1!==var2) {
    console.log("warunek " + var1 + "!==" + var2 +" został spełniony!")
} else {
    console.log("Warunek " + var1 + "!==" + var2 +" nie został spełniony ")
}

/* Operator większości */
if(var1>var2) {
    console.log("warunek " + var1 + ">" + var2 +" został spełniony!")
} else {
    console.log("Warunek " + var1 + ">" + var2 +" nie został spełniony ")
}

/* Operator mniejszości */
if(var1<var2) {
    console.log("warunek " + var1 + "<" + var2 +" został spełniony!")
} else {
    console.log("Warunek " + var1 + "<" + var2 +" nie został spełniony ")
}

/* Operator większy bądź równy */
if(var1>=var2) {
    console.log("warunek " + var1 + ">=" + var2 +" został spełniony!")
} else {
    console.log("Warunek " + var1 + ">=" + var2 +" nie został spełniony ")
}

/* Operator mniejszy bądź równy */
if(var1<=var2) {
    console.log("warunek " + var1 + "<=" + var2 +" został spełniony!")
} else {
    console.log("Warunek " + var1 + "<=" + var2 +" nie został spełniony ")
}

